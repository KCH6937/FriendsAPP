import UIKit

extension UIFont {

    static func notoSansRegular(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "NotoSansCJKkr-Regular", size: size)!
    }
    
    static func notoSansMedium(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "NotoSansCJKkr-Medium", size: size)!
    }

}
